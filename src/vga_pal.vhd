PROCESS(RESET_n, CLK)
variable vADDR : STD_LOGIC_VECTOR(8 DOWNTO 0);

BEGIN
  IF (RESET_n = '0') THEN
    vADDR := (OTHERS => '0');
  ELSIF (RISING_EDGE(CLK)) THEN
    IF CS = '0' THEN
      IF A = "01" THEN
        vADDR := '0' & D;
      ELSIF A = "10" THEN
        vADDR := '1' & D;
      ELSIF A = "00" THEN
          CASE D(7 DOWNTO 6) IS
          WHEN "00" => 
            CRAM_R(to_integer(unsigned(vADDR))) <= D( 5 DOWNTO  0);
          WHEN "01" =>
            CRAM_G(to_integer(unsigned(vADDR))) <= D( 5 DOWNTO  0);
          WHEN "10" =>
            CRAM_B(to_integer(unsigned(vADDR))) <= D( 5 DOWNTO  0);
          WHEN "11" =>
            vADDR := vADDR + 1;
          END CASE;
      END IF;
    END IF;
  END IF;

END PROCESS;
	
END;
